//desabilita o botão no início
document.getElementById("botao2").disabled = true;

document.getElementById("input9").addEventListener("input", function(event){
    
  //busca conteúdo do input
    var conteudo = document.getElementById("input9").value;
  
    //valida conteudo do input 
    if (conteudo !== null && conteudo !== '') {
      //habilita o botão
      document.getElementById("botao2").disabled = false;
    } else {
      //desabilita o botão se o conteúdo do input ficar em branco
      document.getElementById("botao2").disabled = true;
    }
});
